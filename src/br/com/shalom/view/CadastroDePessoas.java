    private void btListarPessoasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarPessoasActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable.getModel();
        int count = tableModel.getRowCount();
        if (count != 0) {
            for (int i = 0; i < count; i++) {
                tableModel.removeRow(0);
            }
        }
        List<Pessoa> pessoas = dao.getAllPeople(JPAUtil.getSession());
        pessoas.forEach((Pessoa p) -> {
            tableModel.addRow(new String[]{p.getId().toString(), p.getNome(), p.getGrupo().getNome(), 
                              p.getMinisterio().getNome(), p.getTelefone(), f.format(p.getNascimento())});
        });
    }//GEN-LAST:event_btListarPessoasActionPerformed

    private void jTableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER && (estado == Estado.ATUALIZACAO || estado == Estado.EXTINCAO)) {
            Pessoa p;
            Session s = JPAUtil.getSession();
            p = (Pessoa) s.load(Pessoa.class, Integer.parseInt(jTable.getValueAt(jTable.getSelectedRow(), 0).toString()));
            jTextId.setText(p.getId().toString());
            jTextNome.setText(p.getNome());
            jDateChooser.setDate(p.getNascimento());
            jTextFone.setText(p.getTelefone());
            jTextRua.setText(p.getEndereco().getRua());
            jTextNum.setText(p.getEndereco().getNumero());
            jTextCity.setText(p.getEndereco().getCidade());
            jComboFuncao.setSelectedItem(p.getFuncao().toString());
            jComboStatus.setSelectedItem(p.getStatus().toString());
            jComboGrupo.setSelectedItem(p.getGrupo().getNome());
            jComboMinisterio.setSelectedItem(p.getMinisterio().getNome());
         }
    }//GEN-LAST:event_jTableKeyPressed

